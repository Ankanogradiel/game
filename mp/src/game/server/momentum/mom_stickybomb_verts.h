static CPhysCollide *GetStickybombCollide()
{
    static CPhysCollide *pCollide = nullptr;

    if (pCollide)
        return pCollide;

    Vector *pVerts[792];
    for (int i = 0; i < 792; i++)
    {
        pVerts[i] = &g_StickyBombVerts[i];
    }

    auto pPhysConvex = physcollision->ConvexFromVerts(pVerts, 792);
    pCollide = physcollision->ConvertConvexToCollide(&pPhysConvex, 1);

    return pCollide;
}